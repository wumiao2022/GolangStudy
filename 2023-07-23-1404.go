package main

import "fmt"

func main() {
	// Print "Hello, World!" to the console
	fmt.Println("Hello, World!")

	// Declare a variable named "age" with the value of 25
	age := 25

	// Print the value of "age" to the console
	fmt.Println("Age:", age)

	// Create an array of integers with the values 1, 2, 3, 4, 5
	numbers := [5]int{1, 2, 3, 4, 5}

	// Print the length of the array
	fmt.Println("Number of elements in array:", len(numbers))

	// Declare a variable named "name" with the value "John Doe"
	name := "John Doe"

	// Print the value of "name" to the console
	fmt.Println("Name:", name)
}