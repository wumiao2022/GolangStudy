package main

import "fmt"

func main() {
	// -----------------------------------------
	// 练习1: 打印Hello, World!
	// -----------------------------------------
	fmt.Println("Hello, World!")

	// -----------------------------------------
	// 练习2: 定义一个整数数组并打印其中的元素
	// -----------------------------------------
	numbers := []int{1, 2, 3, 4, 5}
	for _, num := range numbers {
		fmt.Println(num)
	}

	// -----------------------------------------
	// 练习3: 定义一个函数，接收两个参数，返回它们的和
	// -----------------------------------------
	add := func(a, b int) int {
		return a + b
	}
	fmt.Println(add(3, 5))

	// -----------------------------------------
	// 练习4: 使用条件语句判断一个数是奇数还是偶数
	// -----------------------------------------
	number := 7
	if number%2 == 0 {
		fmt.Println(number, "是偶数")
	} else {
		fmt.Println(number, "是奇数")
	}

	// -----------------------------------------
	// 练习5: 使用循环语句计算1到100的和
	// -----------------------------------------
	sum := 0
	for i := 1; i <= 100; i++ {
		sum += i
	}
	fmt.Println("1到100的和为:", sum)
}