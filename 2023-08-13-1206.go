package main

import "fmt"

func main() {
    // 练习1：打印出 "Hello, World!"
    fmt.Println("Hello, World!")

    // 练习2：定义一个整数变量x并赋值为10，打印x的值
    x := 10
    fmt.Println("x =", x)

    // 练习3：定义一个字符串变量name并赋值为"John"，打印name的值
    name := "John"
    fmt.Println("name =", name)

    // 练习4：定义一个浮点数变量pi并赋值为3.14159，打印pi的值
    pi := 3.14159
    fmt.Println("pi =", pi)

    // 练习5：定义一个布尔类型变量isTrue并赋值为true，打印isTrue的值
    isTrue := true
    fmt.Println("isTrue =", isTrue)
}