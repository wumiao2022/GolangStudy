package main

import (
	"fmt"
)

func main() {
	// 1. 打印"Hello, World!"
	fmt.Println("Hello, World!")

	// 2. 声明一个变量name并赋值为"John"，然后打印出name的值
	name := "John"
	fmt.Println("Name:", name)

	// 3. 声明一个整型变量age并初始化为30，然后打印出age的值
	age := 30
	fmt.Println("Age:", age)

	// 4. 声明一个浮点型变量weight并初始化为70.5，然后打印出weight的值
	weight := 70.5
	fmt.Println("Weight:", weight)

	// 5. 声明一个布尔型变量isMale并初始化为true，然后打印出isMale的值
	isMale := true
	fmt.Println("Is Male:", isMale)
}