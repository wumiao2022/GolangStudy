package main

import "fmt"

func main() {
	// 练习1：打印出Hello, World!
	fmt.Println("Hello, World!")

	// 练习2：声明一个整数变量，赋值为100，打印出该变量的值
	var num int = 100
	fmt.Println(num)

	// 练习3：声明一个字符串变量，赋值为"Go is awesome!"，打印出该变量的值
	var str string = "Go is awesome!"
	fmt.Println(str)

	// 练习4：声明一个浮点数变量，赋值为3.14，打印出该变量的值
	var floatNum float64 = 3.14
	fmt.Println(floatNum)

	// 练习5：声明一个布尔型变量，赋值为true，打印出该变量的值
	var isTrue bool = true
	fmt.Println(isTrue)
}