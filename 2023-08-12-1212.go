package main

import "fmt"

func main() {
	// 练习1：打印Hello, World!
	fmt.Println("Hello, World!")

	// 练习2：声明一个整数变量并赋值为10，打印该变量的值
	var num int = 10
	fmt.Println(num)

	// 练习3：声明一个字符串变量并赋值为"Go语言练习"
	var str string = "Go语言练习"
	fmt.Println(str)

	// 练习4：声明一个布尔变量并赋值为true，打印该变量的值
	var flag bool = true
	fmt.Println(flag)

	// 练习5：声明一个浮点数变量并赋值为3.14，打印该变量的值
	var floatNum float64 = 3.14
	fmt.Println(floatNum)
}