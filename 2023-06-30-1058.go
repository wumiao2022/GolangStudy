package main

import "fmt"

func main() {
	// 1. 声明一个整数变量x，并将其初始化为10
	x := 10

	// 2. 声明一个浮点数变量y，并将其初始化为3.14
	y := 3.14

	// 3. 声明一个布尔变量isTrue，并将其初始化为true
	isTrue := true

	// 4. 声明一个字符串变量name，并将其初始化为"MyName"
	name := "MyName"

	// 5. 打印变量x的值并换行
	fmt.Println(x)

	// 6. 打印变量y的值并换行
	fmt.Println(y)

	// 7. 打印变量isTrue的值并换行
	fmt.Println(isTrue)

	// 8. 打印变量name的值并换行
	fmt.Println(name)
}
