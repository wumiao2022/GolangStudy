package main

import (
	"fmt"
)

func main() {
	// 练习1：打印 Hello, Go!
	fmt.Println("Hello, Go!")

	// 练习2：计算 1+2 的结果并打印
	sum := 1 + 2
	fmt.Println(sum)

	// 练习3：声明一个整型变量并赋值为10，打印该变量的值
	num := 10
	fmt.Println(num)

	// 练习4：声明一个字符串变量并赋值为"Hello, Golang!"，打印该变量的值
	str := "Hello, Golang!"
	fmt.Println(str)

	// 练习5：声明一个布尔型变量并赋值为true，打印该变量的值
	isTrue := true
	fmt.Println(isTrue)
	
	// 练习6：声明一个浮点型变量并赋值为3.14，打印该变量的值
	floatNum := 3.14
	fmt.Println(floatNum)
}