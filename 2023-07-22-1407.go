package main

import "fmt"

func main() {
    // 1. 声明一个整型变量x，并初始化为10
    x := 10

    // 2. 声明一个字符串变量name，并初始化为"Go语言"
    name := "Go语言"

    // 3. 声明一个布尔型变量isValid，并初始化为true
    isValid := true

    // 4. 声明一个浮点型变量pi，并初始化为3.1415
    pi := 3.1415

    // 5. 声明一个整型切片numbers，并初始化为[1, 2, 3, 4, 5]
    numbers := []int{1, 2, 3, 4, 5}

    fmt.Println(x, name, isValid, pi, numbers)
}
