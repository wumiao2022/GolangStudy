package main

import "fmt"

func main() {
	// 1. 声明一个整数变量a，并将其值设置为10
	var a int = 10
	fmt.Println(a)

	// 2. 声明两个整数变量b和c，并将其值分别设置为20和30
	var b, c int = 20, 30
	fmt.Println(b, c)

	// 3. 声明一个浮点数变量d，并将其值设置为3.14
	var d float64 = 3.14
	fmt.Println(d)

	// 4. 声明一个字符串变量e，并将其值设置为"Hello, World!"
	var e string = "Hello, World!"
	fmt.Println(e)

	// 5. 声明一个布尔变量f，并将其值设置为true
	var f bool = true
	fmt.Println(f)
}