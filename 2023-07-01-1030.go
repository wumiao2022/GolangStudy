package main

import "fmt"

func main() {
	// 练习1：声明并输出一个整数变量x的值为100
	x := 100
	fmt.Println(x)

	// 练习2：声明并输出一个字符串变量str的值为"Hello, World!"
	str := "Hello, World!"
	fmt.Println(str)

	// 练习3：声明并输出一个布尔变量flag的值为true
	flag := true
	fmt.Println(flag)

	// 练习4：声明并输出一个浮点数变量pi的值为3.14159
	pi := 3.14159
	fmt.Println(pi)

	// 练习5：声明并输出一个字符变量ch的值为'A'
	ch := 'A'
	fmt.Println(string(ch))
}