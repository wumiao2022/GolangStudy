package main

import "fmt"

func main() {
    // 1. 声明一个整数变量x，将其初始化为10
    x := 10

    // 2. 声明一个浮点数变量y，将其初始化为3.14
    y := 3.14

    // 3. 声明一个字符串变量z，将其初始化为"Hello, World!"
    z := "Hello, World!"

    // 4. 打印变量x的值
    fmt.Println(x)

    // 5. 打印变量y的值
    fmt.Println(y)

    // 6. 打印变量z的值
    fmt.Println(z)

    // 7. 将变量x的值加上5，并将结果赋值给变量x
    x += 5

    // 8. 将变量y的值乘以2.5，并将结果赋值给变量y
    y *= 2.5

    // 9. 将变量z的值与字符串" Golang!"连接，并将结果赋值给变量z
    z += " Golang!"

    // 10. 打印变量x的值
    fmt.Println(x)

    // 11. 打印变量y的值
    fmt.Println(y)

    // 12. 打印变量z的值
    fmt.Println(z)
}
