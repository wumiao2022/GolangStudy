package main

import "fmt"

func main() {
    // 1. 声明一个整数变量num，并将其初始化为10
    num := 10

    // 2. 声明一个字符串变量name，并将其初始化为"GoLang"
    name := "GoLang"

    // 3. 声明一个布尔类型变量status，并将其初始化为true
    status := true

    // 4. 打印num的值
    fmt.Println(num)

    // 5. 打印name的值
    fmt.Println(name)

    // 6. 打印status的值
    fmt.Println(status)
}